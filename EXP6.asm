; ALP to find the square of a number 6 from (1 to 10) using a look-up table. 
		AREA SQUARE, CODE, READONLY
ENTRY
		EXPORT	MAIN
MAIN
		LDR R0, =TABLE1
		MOV R1, #6
		LSL	R1, R1, #2	
		ADD	R0, R0, R1
		LDR R3, [R0]
STOP
		NOP
		B STOP
		ALIGN
TABLE1
		DCD 0x00000000
		DCD 0x00000001
		DCD 0x00000004
		DCD 0x00000009
		DCD 0x00000010
		DCD 0x00000019
		DCD 0x00000024
		DCD 0x00000031
		DCD 0x00000040
		DCD 0x00000051
		DCD 0x00000064
		END
